# Function to print Overfitting Check
def check_overfitting(model, name, X_train, y_train, X_test, y_test):
    train_score = r2_score(y_train, model.predict(X_train))
    test_score = r2_score(y_test, model.predict(X_test))
    gap = train_score - test_score

    print(f"\n--- Overfitting Check: {name} ---")
    print(f"Train R2: {train_score:.4f}")
    print(f"Test R2:  {test_score:.4f}")
    print(f"Gap:      {gap:.4f} {'⚠️ (High Overfit)' if gap > 0.05 else '✅ (Stable)'}")

