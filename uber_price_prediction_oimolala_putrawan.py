def engineer_time_features(df, datetime_col='pickup_datetime'):
    """
    Transforms raw datetime into high-impact features optimized for
    ML algorithms. I might use XGBoost.
    """
    # Ensure column is datetime
    df[datetime_col] = pd.to_datetime(df[datetime_col])

    # Temporal Extractions
    df['hour'] = df[datetime_col].dt.hour
    df['day_of_week'] = df[datetime_col].dt.dayofweek
    df['day_of_month'] = df[datetime_col].dt.day
    df['month'] = df[datetime_col].dt.month

    # Boolean Features (Binary flags)
    df['is_weekend'] = df['day_of_week'].isin([5, 6]).astype(int)

    # Categorical Binning (Surcharge & Demand Zones)
    # Based on New York City taxi/rideshare surcharge rules
    # Will inspect dataset first via EDA. Use if above assumption valid.
    conditions = [
        (df['hour'].between(6, 9)),
        (df['hour'].between(10, 16)),
        (df['hour'].between(17, 20)),
        (df['hour'].between(21, 24)) | (df['hour'].between(0, 5)),
    ]
    choices = ['morning_rush', 'midday', 'evening_rush', 'late_night']
    df['time_period'] = np.select(conditions, choices, default='standard')

    # Total Minutes (Continuous feature)
    df['minute_of_day'] = df['hour'] * 60 + df[datetime_col].dt.minute

    return df

