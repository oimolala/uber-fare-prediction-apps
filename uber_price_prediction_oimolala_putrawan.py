# Use your specific variable name as the input
# We'll use the full PCA transformed set to find global patterns

X_clustering = df_pca_transform
inertia = []
silhouette_avg = []
k_range = range(2, 11) # Testing from 2 to 10 clusters
print("🕒 Analyzing trip patterns across 9 Principal Components...")

for k in k_range:
    # Initialize KMeans
    kmeans = KMeans(n_clusters=k, init='k-means++', random_state=42, n_init=10)
    cluster_labels = kmeans.fit_predict(X_clustering)
    # 1. Inertia: Sum of squared distances to closest cluster center (Elbow Method)

    inertia.append(kmeans.inertia_)
    # 2. Silhouette Score: How similar an object is to its own cluster vs. others
    # Note: This can be slow on very large datasets
    score = silhouette_score(X_clustering, cluster_labels)
    silhouette_avg.append(score)
    print(f"K={k} analyzed. Silhouette Score: {score:.4f}")

# --- Plotting the "Decision Dashboard" ---
fig, ax1 = plt.subplots(figsize=(12, 6))

# Plot Inertia (Elbow)
ax1.set_xlabel('Number of Clusters (K)')
ax1.set_ylabel('Inertia', color='tab:blue', fontsize=12)
