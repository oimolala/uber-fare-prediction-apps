"""A simple linear model assumes every meter costs the same. L-curve analysis proves that short trips are dominated by a fixed base fare, causing a 4x spike in price-per-km for rides under 4km. To handle this, I used XGBoost, which can naturally 'segment' the distance into high-cost and low-cost zones.

### Plotting Gradient L-curve to determine estimated base fare

I want to see for every distance group (by binning them), how does the mean of `price_per_m` change? When the gradient of the mean is approaching zero, it means the impact of the base fare to `price_per_km` diminish. Then I will take 10% of that threshold distance as base fare amount.
"""

bin_sizes = np.arange(100, 2501, 100)
sweep_results = []

for size in bin_sizes:
    # Discretize distance at current resolution
    df_temp = df_eda.copy()
    df_temp['temp_bin'] = (df_temp['manhattan_distance_m'] // size) * size

    # Calculate median price per km (Median is more robust against the 'London' outliers)
    bin_stats = df_temp.groupby('temp_bin')['price_per_km'].median().reset_index()

    # We filter the bins themselves to stay within your window of interest
    bin_stats = bin_stats[(bin_stats['temp_bin'] >= 150) & (bin_stats['temp_bin'] <= 2500)]

    if len(bin_stats) > 5: # Need enough points for a stable derivative
        # 1st Derivative: Change in Price / Change in Bin
        dy = np.diff(bin_stats['price_per_km'])
        dx = np.diff(bin_stats['temp_bin'])
        grad1 = dy / dx

        # 2nd Derivative: Rate of change of the slope
        d2y = np.diff(grad1)
        d2x = np.diff(bin_stats['temp_bin'][:-1])
        grad2 = d2y / d2x

        # Identify the peak of curvature
        max_idx = np.argmax(np.abs(grad2))
        sweep_results.append({
            'bin_size': size,
            'max_inflection_val': np.abs(grad2[max_idx]),
            'inflection_point_m': bin_stats['temp_bin'].iloc[max_idx + 1] # +1 due to diff()
        })

# Analyse the steepest curvature
sweep_df = pd.DataFrame(sweep_results)
best_bin_config = sweep_df.loc[sweep_df['max_inflection_val'].idxmax()]

print(f" Optimal Resolution: {best_bin_config['bin_size']}m")
